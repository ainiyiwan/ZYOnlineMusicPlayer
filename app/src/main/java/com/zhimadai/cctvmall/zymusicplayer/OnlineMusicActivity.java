import android.view.View;
import android.view.ViewGroup;
import android.widget.AbsListView;
import android.widget.AdapterView;
import android.widget.ImageView;
import android.widget.LinearLayout;
import android.widget.TextView;

import com.bumptech.glide.Glide;
import com.bumptech.glide.request.animation.GlideAnimation;
import com.bumptech.glide.request.target.SimpleTarget;
import com.zhimadai.cctvmall.zymusicplayer.adapter.OnMoreClickListener;
import com.zhimadai.cctvmall.zymusicplayer.adapter.OnlineMusicAdapter;
import com.zhimadai.cctvmall.zymusicplayer.application.AppCache;
import com.zhimadai.cctvmall.zymusicplayer.common.constants.Extras;
import com.zhimadai.cctvmall.zymusicplayer.enums.LoadStateEnum;
import com.zhimadai.cctvmall.zymusicplayer.executor.DownloadOnlineMusic;
